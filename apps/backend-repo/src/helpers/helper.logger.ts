import consola from 'consola'

export const logger = (src: any, type: 'error' | 'info' | 'wanr' | 'log' | 'debug'): void => {
  switch (type) {
    case 'error':
      consola.error(`
          ==================================
          ======== Error Exception =========
          ==================================

              name: ${src.name}
              message: ${src.message}
              stack: ${src.stack}

          ==================================
          ==================================
          ==================================
       `)
      break

    case 'debug':
      consola.debug(`
          ==================================
          ======== Logger Debug =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
      break

    case 'info':
      consola.info(`
          ==================================
          ======== Logger Info =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
      break

    case 'wanr':
      consola.warn(`
          ==================================
          ======== Logger Warn =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
      break

    default:
      consola.log(`
          ==================================
          ======== Logger Log =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
      break
  }
}
