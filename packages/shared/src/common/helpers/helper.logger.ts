export const logger = (src: any, type: 'error' | 'info' | 'wanr' | 'log' | 'debug'): void => {
	switch (type) {
		case 'error':
			console.error(`
          ==================================
          ======== Error Exception =========
          ==================================

              name: ${src.name}
              message: ${src.message}
              stack: ${src.stack}

          ==================================
          ==================================
          ==================================
       `)
			break

		case 'debug':
			console.debug(`
          ==================================
          ======== Logger Debug =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
			break

		case 'info':
			console.info(`
          ==================================
          ======== Logger Info =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
			break

		case 'wanr':
			console.warn(`
          ==================================
          ======== Logger Warn =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
			break

		default:
			console.log(`
          ==================================
          ======== Logger Log =========
          ==================================

            content: ${JSON.stringify(src) || src}

          ==================================
          ==================================
          ==================================
       `)
			break
	}
}
